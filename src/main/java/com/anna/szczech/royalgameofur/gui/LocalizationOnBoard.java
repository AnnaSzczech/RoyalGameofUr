package com.anna.szczech.royalgameofur.gui;

public class LocalizationOnBoard {
    private double x;
    private double y;

    public LocalizationOnBoard(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
